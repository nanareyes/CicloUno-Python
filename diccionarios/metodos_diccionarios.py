'''
Podemos obtener todas las llaves de nuestro diccionario utilizando el metodo keys()
'''
empleados = {'Sandra ':1, 'Teresa':2, 'Juan':2}
print(empleados.keys())


# Ejemplo 3 Clave y valor items() devuelve una lista de clave: valor del diccionario

empleados = {'Sandra ':1, 'Teresa':2, 'Juan':2}
print(empleados.items())